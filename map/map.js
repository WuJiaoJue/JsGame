function putMap(bge,xNum,yNum,map){
    mapE.id = 'mapE'
    stage.append(mapE)
    for (let y = 0; y < 2 * yNum; y++){
        for (let x = 0; x < 2 * xNum; x++){
            switch (map[y][x]){
                case 0:
                    continue
                case 21:
                    collision.push(x - xNum,y - yNum)
                    collisions.push(collision)
                    collision = []
                    break
                case 22:
                    collision.push(x - xNum,y - yNum)
                    collisions.push(collision)
                    collision = []
                    break
            }
            putBGE(stage,bge[map[y][x]],x - xNum,y - yNum,80,80)
        }
    }
    console.log("碰撞方块位置：",collisions,arrE)
}

